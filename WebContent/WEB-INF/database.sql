
/* Drop Tables */

DROP TABLE ADMINUSER;
DROP TABLE SERVICE;




/* Create Tables */

CREATE TABLE ADMINUSER
(
	USERID INT NOT NULL AUTO_INCREMENT,
	USERNAME VARCHAR(20),
	PASSWORD VARCHAR(20),
	PRIMARY KEY (USERID)
);


CREATE TABLE SERVICE
(
	USERID INT NOT NULL AUTO_INCREMENT,
	USERNAME VARCHAR(20),
	ZHAOPIAN VARCHAR(200),
	PINGPAIMING VARCHAR(20),
	XILIE VARCHAR(20),
	BAOXIAN VARCHAR(100),
	FUZEREN VARCHAR(20),
	PRIMARY KEY (USERID)
);



